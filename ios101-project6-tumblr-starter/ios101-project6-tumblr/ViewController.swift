import UIKit
import Nuke

class ViewController: UIViewController, UITableViewDataSource {

    @IBOutlet weak var tableView: UITableView!

    var posts: [Post] = []

    override func viewDidLoad() {
        super.viewDidLoad()

        tableView.dataSource = self
        fetchPosts()

    }
    @IBOutlet weak var UIImageView: UIImageView!
    @IBOutlet weak var UITextView: UILabel!
    override func viewWillAppear(_ animated: Bool) {
        super.viewWillAppear(animated)

        if let indexPath = tableView.indexPathForSelectedRow {
            tableView.deselectRow(at: indexPath, animated: true)
        }
    }
    override func prepare(for segue: UIStoryboardSegue, sender: Any?) {
        print("üîÅ prepare(for segue:) called")

        if segue.identifier == "showDetail" {
            print("üéØ Segue identifier matched: showDetail")

            if let cell = sender as? UITableViewCell,
               let indexPath = tableView.indexPath(for: cell) {
                print("üì¶ Sending post at index \(indexPath.row)")

                let post = posts[indexPath.row]
                let detailVC = segue.destination as! DetailViewController
                detailVC.post = post
            } else {
                print("‚ùå Failed to get indexPath for selected cell")
            }
        } else {
            print("‚ùå Segue identifier didn‚Äôt match")
        }
    }




    func tableView(_ tableView: UITableView, numberOfRowsInSection section: Int) -> Int {
        return posts.count
    }

    func tableView(_ tableView: UITableView, cellForRowAt indexPath: IndexPath) -> UITableViewCell {
        let cell = tableView.dequeueReusableCell(withIdentifier: "PostCell", for: indexPath) as! PostCell

        let post = posts[indexPath.row]

        cell.summaryLabel.text = post.summary

        if let photo = post.photos.first {
            let url = photo.originalSize.url
            Nuke.loadImage(with: url, into: cell.postImageView)
        }

        return cell
    }

    func fetchPosts() {
        let url = URL(string: "https://api.tumblr.com/v2/blog/humansofnewyork/posts/photo?api_key=1zT8CiXGXFcQDyMFG7RtcfGLwTdDjFUJnZzKJaWTmgyK4lKGYk")!
        let session = URLSession.shared.dataTask(with: url) { data, response, error in
            if let error = error {
                print("‚ùå Error: \(error.localizedDescription)")
                return
            }

            guard let statusCode = (response as? HTTPURLResponse)?.statusCode, (200...299).contains(statusCode) else {
                print("‚ùå Response error: \(String(describing: response))")
                return
            }

            guard let data = data else {
                print("‚ùå Data is NIL")
                return
            }

            do {
                let blog = try JSONDecoder().decode(Blog.self, from: data)

                DispatchQueue.main.async { [weak self] in

                    let posts = blog.response.posts
                    self?.posts = posts
                    self?.tableView.reloadData()

                    print("‚úÖ We got \(posts.count) posts!")
                    for post in posts {
                        print("üçè Summary: \(post.summary)")
                    }
                }

            } catch {
                print("‚ùå Error decoding JSON: \(error.localizedDescription)")
            }
        }
        session.resume()
    }
}
